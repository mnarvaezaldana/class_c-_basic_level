#include <iostream>

using namespace std;

int main() {

    int numeroINT = 1000;
    cout << numeroINT << endl;
    cout << "Variable Entera " << numeroINT << endl;

    short numeroSHORT = 1200;
    cout << numeroSHORT << endl;
    cout << "Variable short " << numeroSHORT << endl;

    long long int numeroLONGLONG = 234567891011;
    cout << numeroLONGLONG << endl;
    cout << "Variable Long " << numeroLONGLONG << endl;

    float numeroFLOAT = 2.165432;
    cout << numeroFLOAT << endl;
    cout << "Variable Float " << numeroFLOAT << endl;

    double numeroDOUBLE = 2.5698715832;
    cout << numeroDOUBLE << endl;
    cout << "Variable Double " << numeroDOUBLE << endl;

    char caracter = 'A';
    cout << caracter << endl;
    cout << "Variable caracter " << caracter << endl;

    bool booleano = true;
    cout << booleano << endl;
    cout << "Variable booleano " << booleano << endl;
    return 0;
}
