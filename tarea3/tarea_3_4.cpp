#include <iostream>

using namespace std;

int main() {

    int valor;

    cout << "Por favor, ingrese un número" << endl;
    cin >> valor;

    if (valor > 0) {
        cout << "El número es positivo" << endl;
    } else {
        cout << "El número es negativo" << endl;
    }

    return 0;
}





